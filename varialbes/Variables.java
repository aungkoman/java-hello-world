public class Variables {
    public static void main(String[] args){
        System.out.println("");
        System.out.println("Hello World");

        int my_box = 25;
        String greeting = "Hello Variables";

        System.out.println(my_box); // 25
        System.out.println("my_box"); // my_box
        System.out.println(greeting); // Hello Variables

        System.out.println("");
    }
}
